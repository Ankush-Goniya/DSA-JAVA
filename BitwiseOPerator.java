public class BitwiseOPerator {
    public static void main(String[] args) {
            int a = 10, b = 4;  // a=1010  b=0100

            System.out.println(a & b);
            System.out.println(a | b);
            System.out.println(a ^ b);

            System.out.println(a << 1); //10
            System.out.println(b << 2); //20


        }

}
